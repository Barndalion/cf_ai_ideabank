    async def chat_handler(self, uuid: str, role: str, message: str):
        key = uuid if uuid else str(gen_chat_uuid())
        raw = await self.ctx.storage.get(key)

        if raw:
            chat = json.loads(raw)
            new_chat = False
        else:
            chat = {}
            new_chat = True

        current_msg = {"role": role, "content": message}
        if new_chat:
            chat["messages"] = []

        chat["messages"].append(current_msg)

        idea_index_raw = await self.ctx.storage.get("idea_index")
        idea_index = json.loads(idea_index_raw) if idea_index_raw else {}

        idea_context = ""
        if idea_index:
            idea_context = "\n\nUSER'S SAVED IDEAS (use these exact IDs for any tool calls):\n"
            for idea_id, title in idea_index.items():
                idea_raw = await self.ctx.storage.get(idea_id)
                if idea_raw:
                    idea_obj = json.loads(idea_raw)
                    desc = idea_obj.get("description", "")[:120]
                    idea_context += f"- ID: {idea_id} | Title: {title} | Description: {desc}\n"
                else:
                    idea_context += f"- ID: {idea_id} | Title: {title}\n"
        else:
            idea_context = "\n\nUSER'S SAVED IDEAS: None saved yet."

        system_content = SYSTEM_PROMPT + idea_context

        if new_chat:
            system_content += (
                "\n\nThis is a new conversation. You MUST include a 'title' key in your JSON output "
                "with a short 3-5 word title summarizing this conversation."
            )

        model_messages = [{"role": "system", "content": system_content}]

        for msg in chat["messages"]:
            model_messages.append({
                "role": msg["role"],
                "content": msg["content"]  
            })

        result = await self.run_model(model_messages)

        assistant_msg = {
            "role": "assistant",
            "content": result.get("message", ""),
        }

        response_from_mapper = await self.map_ai_to_function(result, user_message=message)

        if not response_from_mapper.get("state") and result.get("tool") is not None:
            assistant_msg["content"] = response_from_mapper.get("message")

        chat["messages"].append(assistant_msg)

        if new_chat:
            title = result.get("title")
            if not title or title == "null":
                first_user = next((m["content"] for m in chat["messages"] if m["role"] == "user"), "")
                title = first_user[:40] + ("..." if len(first_user) > 40 else "")
            chat["title"] = title

        await self.ctx.storage.put(key, json.dumps(chat))
        return {
            "tool": result.get("tool") or "",
            "args": result.get("args") or {},
            "message": result.get("message") or "",
            "title": result.get("title") or "",
        }

    async def get_history(self):
        result = {}
        data = await self.ctx.storage.list()
        for k, v in data.items():
            try:
                if k.startswith("chat:"):
                    result[k] = json.loads(v) if isinstance(v, str) else v
            except (ValueError, json.JSONDecodeError) as e:
                result["error"] = str(e)
        return result if result else {}
    
    async def get_by_id(self, id):
        raw = await self.ctx.storage.get(id)
        return json.loads(raw) if raw else {}
    
    async def delete_chat(self, chat_id: str):
        try:
            await self.ctx.storage.delete(chat_id)
        except Exception as e:
            return {"state": False, "message": str(e)}
        return {"state": True, "message": "Chat deleted successfully"}

