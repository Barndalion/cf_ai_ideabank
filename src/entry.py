    async def run_model(self, messages: list) -> dict:
        import js
        result = await self.env.AI.run(
            "@cf/meta/llama-3.3-70b-instruct-fp8-fast",
            js.JSON.parse(json.dumps({"messages": messages, "stream": False})),
        )

        try:
            raw_json = js.JSON.stringify(result)
            result_dict = json.loads(str(raw_json))
        except Exception:
            result_dict = {}

        # llama-3.3 returns the text in "response"
        raw = ""
        if isinstance(result_dict, dict):
            raw = result_dict.get("response") or result_dict.get("result") or ""
        elif isinstance(result_dict, str):
            raw = result_dict

        text = str(raw).strip()

        # Strip accidental markdown code fences the model sometimes adds
        if text.startswith("```"):
            text = text.split("```")[1]
            if text.startswith("json"):
                text = text[4:]
            text = text.strip()

        import ast

        # Try ast.literal_eval first — handles both proper JSON-like dicts AND
        try:
            parsed = ast.literal_eval(text)
            if isinstance(parsed, dict):
                return {
                    "tool": parsed.get("tool"),
                    "args": parsed.get("args"),
                    "message": parsed.get("message", ""),
                    "title": parsed.get("title"),
                }
        except Exception:
            pass

        # Fall back to json.loads for strict JSON strings
        try:
            return json.loads(text)
        except Exception:
            pass

        # Last resort: return the raw text as a plain message
        return {
            "tool": None,
            "args": None,
            "message": text or "I encountered an error processing your request."
        }

    async def resolve_idea_id(self, user_request: str):
        raw = await self.ctx.storage.get("idea_index")
        if raw is None:
            return None

        idea_index = json.loads(raw) if isinstance(raw, str) else raw
        if not idea_index:
            return None

        # Load full idea details for matching
        ideas = {}
        for idea_id, title in idea_index.items():
            idea_raw = await self.ctx.storage.get(idea_id)
            desc = ""
            if idea_raw:
                obj = json.loads(idea_raw)
                desc = obj.get("description", "")
            ideas[idea_id] = {"title": title, "description": desc}

        # Keyword score against title + description
        user_lower = user_request.lower()
        best_id = None
        best_score = 0
        for idea_id, data in ideas.items():
            combined = (data["title"] + " " + data["description"]).lower()
            score = sum(1 for word in combined.split() if word in user_lower)
            if data["title"].lower() in user_lower:
                score += 10
            if score > best_score:
                best_score = score
                best_id = idea_id

        # Require at least 2 matching words to avoid spurious matches on
        # common words like "the", "my", "idea" that appear in every title
        if best_id and best_score >= 2:
            return best_id

        # Single strong word match on the title is also acceptable
        if best_id and best_score == 1:
            user_words = set(user_lower.split())
            title_words = set(ideas[best_id]["title"].lower().split())
            # Only accept if the matching word is meaningful (>3 chars)
            overlap = user_words & title_words
            if any(len(w) > 3 for w in overlap):
                return best_id

        # AI fallback with full title+description context
        candidates = ""
        for idea_id, data in ideas.items():
            candidates += f"ID: {idea_id} | Title: {data['title']} | Description: {data['description'][:100]}\n"

        messages = [
            {
                "role": "system",
                "content": "Return ONLY valid JSON with a single key \'id\'. No markdown, no extra text."
            },
            {
                "role": "user",
                "content": (
                    f"The user is referring to one of their saved ideas.\n"
                    f"User request: {user_request}\n\n"
                    f"Saved ideas:\n{candidates}\n"
                    f'Return ONLY: {{"id": "<the matching idea_id>"}}' 
                )
            }
        ]

        result = await self.run_model(messages)
        matched = result.get("id")
        if matched not in idea_index:
            return None
        return matched
    
    async def map_ai_to_function(self, ai_response: dict, user_message: str = "") -> dict:
        tool_map = {
            "add_idea": self.add_idea,
            "delete_idea": self.delete_idea,
            "refine_idea": self.refine_idea,
            "update_idea": self.update_idea,
            "list_ideas": self.list_ideas,
            "get_idea": self.get_idea,
        }
        uuid_tools = {"delete_idea", "refine_idea", "update_idea"}

        tool = ai_response.get("tool")
        args = ai_response.get("args") or {}

        if tool is None:
            return {"state": False, "message": f"Cannot perform action {tool}"}

        fn = tool_map.get(tool)
        if fn is None:
            return {"state": False, "message": f"Unable to complete task, tool: {tool} not available"}

        if tool in uuid_tools:
            idea_id = await self.resolve_idea_id(user_message)

            if idea_id is None:
                return {"state": False, "message": "Could not identify which idea you meant. Please be more specific."}

            if tool == "refine_idea":
                instruction = args.get("instruction", "")
                res = await fn(idea_id, instruction)
                return {"state": True, "message": res.get("message")}

            if tool == "update_idea":
                res = await fn(idea_id, args)
                return {"state": True, "message": res.get("message")}

            if tool == "delete_idea":
                res = await fn(idea_id)
                return {"state": True, "message": res.get("message")}

        if tool == "list_ideas":
            return {"state": True, "message": await fn()}
        res = await fn(args)
        return {"state": True, "message": res.get("message")}
    
    async def search_ideas(self, query: str):
        raw = await self.ctx.storage.get("idea_index")
        if not raw:
            return []

        idea_index = json.loads(raw)
        matches = [
            idea_id for idea_id, title in idea_index.items()
            if query.lower() in title.lower()
        ]

        ideas = []
        for idea_id in matches:
            idea_raw = await self.ctx.storage.get(idea_id)
            if idea_raw:
                idea = json.loads(idea_raw)
                if "id" not in idea:
                    idea["id"] = idea_id
                ideas.append(idea)

        return ideas

